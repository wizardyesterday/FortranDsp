C
C-----------------------------------------------------------------------
C MAIN PROGRAM: THIS PROGRAM TO PERFORM SAMPLING RATE CONVERSION
C               BY A RATIONAL RATIO
C AUTHOR        R,E, CROCHIERE (ORIGINAL AUTHOR)
C               BELL LABORATORIES, MURRAY HILL, NEW JERSEY 07914
C INPUT:        COEF = ARRAY OF COEFFICIENTS FOR FIR INTERPOLATING FILTER
C-----------------------------------------------------------------------
C
      COMMON /SRCOM/ IQ, JQ, IL
      DIMENSION COEF(68), COFS(135),QBUF(54), ICTR(10),BUFL(15),
     *          BUFM(6)
      IQ = 0
      JQ = 0
      IL = 0
C
C THIS PROGRAM CONVERTS THE SAMPLING RATE OF A SIGNAL BY A RATIO OF 5/2
C THE PROGRAM CALLS SRINIT TO INITIALIZE AND THEN CALLS SRCONV SUPPLYING
C INPUT DATA THROUGH BUFM AND TAKING OUTPUT DATA FROM BUFL
C
C INPUT: COEFFICIENTS AND PCM VALUES ARE READ FROM THE STANDARD INPUT
C UNIT (IND). THE INPUT FILE CONTAINS THE COEFFICIENT VALUES FOLLOWED
C BY THE PCM VALUES. tHIS SIMPLIFIES INPUT PROCESSING.
C
C OUTPUT: ALL OUTPUT IS WRITTEN ON THE STANDARD OUTPUT UNIT (LPT)
C
C STANDARD INPUT UNIT NUMBER
      IND = 5
C
C STANDARD OUTPUT UNIT NUMBER
      LPT = 6
C
C THE COUNT OF THE CENTER COEFFICIENT PLUS THE UPPER COEFICIENTS OF H(K)
      NN = 68
C
C READ IN THE COEFFICIENTS
      READ (IND,9999) (COEF(K),K=1,NN)
C
C INITIALIZE CONVERSION ROUTINE.
C
C NUMBER OF DELAY ELEMENTS IN THE PROTOTYPE FILTER
      N = 134
C
C NUMBER OF COEFFICIENTS IN THE PROTOTYPE FILTER
      NC = 135
C
C NUMBER OF ELEMENTS IN THE CONTROL ARAY
      NI = 10
C
C INTERPOLATION FACTOR
      L = 5
C
C DECIMATION FACTOR 
      M = 2
C
C THE SIZE OF THE FILTER STATE MEMORY
      NQ = 54
C
C GET THINGS SET UP!
      CALL SRINIT(M, L, QBUF, NQ, COEF, N, COFS, NC, ICTR, NI, IERR)
C
C PROCESS DATA
C
C THIS IS THE ARBITRARY INTEGER USED BY THE POLYPHASE RESAMPLER
      ND = 3
C
C I THINK THIS IS REALLY ND*l. i'M NOT SURE WHY A MAGIC NUMBER IS USED
      NN = 15
C
C LET'S DO IT TO IT!
C WE HAVE 26666 RECORDS OF LENGTH 6
      DO 8 I=1,26666
C
C GRAB  SIX SAMPLES TO PRESENT TO THE POLYPHASE FILTER
        READ (IND,9998) (BUFM(K),K=1,6)
C
C RUN THEPOLYPHASE RESAMPLER
        CALL SRCONV(BUFM, BUFL, ND, QBUF, COFS, ICTR)
C
C WRITE INTERPOLATED DATA TO STANDARD OUTPUT
        WRITE (LPT,9997) (BUFL(K),K=1,NN)
C
8     CONTINUE
C
9999  FORMAT (F9.0)
9998  FORMAT (F6.0)
9997  FORMAT (F13.6)
C
      STOP
      END
